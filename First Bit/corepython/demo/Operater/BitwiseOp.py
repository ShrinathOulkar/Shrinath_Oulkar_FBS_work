### Bitwise Operator

 # 1 . & (and)
 
print( 5 & 3)
print( 10 & 12)

# 2 . | (or)

print ( 5| 3)

# 3. ^ (XOR)

print( 5 ^ 3)

# 4 . ~ (NOT)

print(~ 5)
print( ~ -10)

# 5. << (Left Shift)

print( 5 << 2)
print( 15 << 1)

# 6 . >> (Right Shift)

print( 5 >> 2)
print( 31 >> 2)
print( 22 >> 1)
